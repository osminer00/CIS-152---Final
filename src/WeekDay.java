
/**
 * Class to hold weekday INformation
 * @author Owen Miner
 * @version
 */

public class WeekDay {
	
	private String day;
	private String exertion;
	private String exType;
	private int accomplished;
	
	//getters and setters
	
	public String getDay() {
		return day;
	}
	public int getAccomplished() {
		return accomplished;
	}
	public void setAccomplished(int accomplished) {
		this.accomplished = accomplished;
	}
	public void setDay(String day) {
		this.day = day;
	}
	public String getExertion() {
		return exertion;
	}
	public void setExertion(String exertion) {
		this.exertion = exertion;
	}
	public String getExType() {
		return exType;
	}
	public void setExType(String exType) {
		this.exType = exType;
	}
	
	
	
	
	
	
	
	
}
